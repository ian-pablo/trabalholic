function abremenu() {
    var n = document.getElementById("nav")

    if (n.style.display == "flex") {
        n.style.display = "none";
    } else {
        n.style.display = "flex";
    }
}

function fechamenu() {
    var f = document.getElementById("nav")

    if (f.style.display == "flex") {
        f.style.display = "none";
    } else {
        f.style.display = "flex";
    }
}